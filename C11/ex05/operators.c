int	plus(int a, int b)
{
	return (a + b);
}

int	minus(int a, int b)
{
	return (a - b);
}

int	times(int a, int b)
{
	return (a * b);
}

int	division(int a, int b)
{
	return (a / b);
}

int	modulus(int a, int b)
{
	return (a % b);
}
